package com.hk.operator;

public class Single1 {

	public static void main(String[] args) {
		// 증감연산자 ++ ==
		int a = 3;
		int b = 6;
		
//		a++; // 현재 값에서 1 증가
//		++b; // 현재 값에서 1 증가

		System.out.println(a++);
		System.out.println(++b);
		System.out.println(a);
		System.out.println(b);
	}

}
